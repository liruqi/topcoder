#include<iostream.h>
int t10[5]={1,10,100,1000,10000};	//decimal
int t12[5]={1,12,144,1728,10000};
int t16[5]={1,16,256,4096,10000};
int d[3][4];
int i,j;
void main()
{
	for(i=2992;i<10000;i++)
	{
		for(j=0;j<4;j++)
		{
			d[0][j]=(i%t10[j+1])/t10[j];
			d[1][j]=(i%t12[j+1])/t12[j];
			d[2][j]=(i%t16[j+1])/t16[j];
		}
		for(j=1;j<4;j++)
		{
			d[0][0]+=d[0][j];
			d[1][0]+=d[1][j];
			d[2][0]+=d[2][j];
		}
		if(d[0][0]==d[1][0])
			if(d[2][0]==d[1][0])
				cout<<i
					<<endl;
	}
}




